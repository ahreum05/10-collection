dic1 = {'a': 1, 'b': 2, 'c': 'hello', 'd': '파이썬'}
print(dic1)
print('-' * 20)

# key 값 확인 
print(dic1.keys())
# 리스트로 변경
print(list(dic1.keys()))
print('-' * 20)

# value 값 확인
print(dic1.values())
# 리스트로 변경
print(list(dic1.values()))
print('-' * 20)

# key, value 둘다 확인
print(dic1.items())
# 리스트로 변경
print(list(dic1.items()))
print('-' * 20)

